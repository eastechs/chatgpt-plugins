#!/usr/bin/env node
//
// Pulls LiteLLM's model_prices_and_context_window.json, slims it to the
// fields and modes we care about, and writes the result as a TypeScript
// module at src/main/ai/pricing/bundled-pricing.ts. A TS export (rather
// than .json) is used so tsup picks it up automatically with bundle:false
// — JSON files would otherwise not be copied to dist/.
//
// Run before a release to refresh the bundled snapshot:
//   node scripts/sync-model-pricing.js
//
// At runtime the app also tries to fetch the live file in the background
// and falls back to this bundle if the network is unreachable.

import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SOURCE_URL =
  "https://raw.githubusercontent.com/BerriAI/litellm/main/model_prices_and_context_window.json";
const KEEP_MODES = new Set(["chat", "completion", "responses", "embedding"]);
const KEEP_FIELDS = [
  "litellm_provider",
  "mode",
  "input_cost_per_token",
  "output_cost_per_token",
  "cache_read_input_token_cost",
  "cache_creation_input_token_cost",
  "max_input_tokens",
  "max_output_tokens",
];

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUTPUT_DIR = path.resolve(
  __dirname,
  "..",
  "src",
  "main",
  "ai",
  "pricing",
);
const OUTPUT_TS = path.join(OUTPUT_DIR, "bundled-pricing.ts");

async function main() {
  console.log(`Fetching ${SOURCE_URL}`);
  const res = await fetch(SOURCE_URL);
  if (!res.ok) throw new Error(`HTTP ${res.status}: ${res.statusText}`);
  const raw = await res.json();

  const slim = {};
  let kept = 0;
  let dropped = 0;
  for (const [key, value] of Object.entries(raw)) {
    if (key === "sample_spec") {
      dropped++;
      continue;
    }
    if (!value || typeof value !== "object") {
      dropped++;
      continue;
    }
    const mode = value.mode;
    if (mode && !KEEP_MODES.has(mode)) {
      dropped++;
      continue;
    }

    const slimEntry = {};
    for (const field of KEEP_FIELDS) {
      if (value[field] !== undefined && value[field] !== null) {
        slimEntry[field] = value[field];
      }
    }
    if (Object.keys(slimEntry).length === 0) {
      dropped++;
      continue;
    }
    slim[key] = slimEntry;
    kept++;
  }

  const fetchedAt = new Date().toISOString();
  const tsContent = `// AUTO-GENERATED by scripts/sync-model-pricing.js — DO NOT EDIT MANUALLY.
// Source: ${SOURCE_URL}
// Fetched: ${fetchedAt}
// Run \`node scripts/sync-model-pricing.js\` to refresh.

import type { RawPricingData } from '../pricing.js';

export const BUNDLED_PRICING: RawPricingData = ${JSON.stringify({
    source: SOURCE_URL,
    fetched_at: fetchedAt,
    models: slim,
  })};
`;

  await fs.mkdir(OUTPUT_DIR, { recursive: true });
  await fs.writeFile(OUTPUT_TS, tsContent);

  // Remove the legacy .json output if it exists from earlier runs.
  await fs.rm(path.join(OUTPUT_DIR, "bundled-pricing.json"), { force: true });

  const sizeKB = (tsContent.length / 1024).toFixed(1);
  console.log(`Wrote ${OUTPUT_TS}`);
  console.log(`  kept: ${kept}, dropped: ${dropped}, size: ${sizeKB} KB`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
